uint8_t modifier_to_bitmask(uint8_t hid_code) {
    switch(hid_code) {
		case 0xE0: return (1<<0);  // left ctrl
        case 0xE1: return (1<<1);  // left shift
        case 0xE2: return (1<<2);  // left opt
        case 0xE3: return (1<<3);  // left cmd
        case 0xE4: return (1<<4);  // right ctrl
        case 0xE5: return (1<<5);  // right shift
        case 0xE6: return (1<<6);  // right opt
		default: return false;
    }
}
